/// Render the whole schema as a DDL script (statements separated by blank lines).
pub fn ddl(schema: &CheckedSchema, dialect: Dialect) -> String {
    let mut out = String::new();
    out.push_str(&format!(
        "-- Generated by `based gen sql` (dialect: {}). Do not edit by hand.\n",
        dialect.name()
    ));
    for model in &schema.models {
        out.push('\n');
        out.push_str(&create_table(schema, model, dialect));
    }
    out
}

/// The set of index-like clauses a model carries, resolved to physical columns and
/// names. Shared by both dialects: MariaDB inlines them into the `CREATE TABLE`;
/// SQLite emits them as trailing `CREATE INDEX` statements.
struct IndexSpec {
    name: String,
    columns: Vec<String>,
    unique: bool,
    /// `using <method>` — an access method the target must have (checked in sema).
    method: Option<String>,
    /// An opaque `@index raw("…")` body: everything after `ON <table>`.
    raw: Option<String>,
}

/// Every declared `@index` on a model, resolved to physical columns + a stable
/// name. A non-unique index on a `@soft_delete` model is rendered predicate-leading:
/// the always-filtered tombstone column is physically prepended (neither MariaDB nor
/// SQLite has partial indexes), so the written index still leads with what selects. A
/// unique index is a constraint — its column set is never reshaped.
fn index_specs(model: &RModel, dialect: Dialect) -> Vec<IndexSpec> {
    model
        .indexes
        .iter()
        .map(|idx| {
            if let Some(spec) = &idx.raw {
                return IndexSpec {
                    name: raw_index_name(&model.table, spec),
                    columns: Vec::new(),
                    unique: false,
                    method: None,
                    raw: Some(
                        spec.for_dialect(dialect.name())
                            .unwrap_or_default()
                            .to_string(),
                    ),
                };
            }
            let mut columns = idx.columns.clone();
            if !idx.unique {
                if let Some(sd) = &model.soft_delete {
                    if columns.first() != Some(&sd.field) {
                        columns.insert(0, sd.field.clone());
                    }
                }
            }
            IndexSpec {
                name: constraint_name(
                    if idx.unique { "uq" } else { "idx" },
                    &model.table,
                    &columns,
                ),
                columns: columns.iter().map(|c| physical_col(model, c)).collect(),
                unique: idx.unique,
                method: idx.method.clone(),
                raw: None,
            }
        })
        .collect()
}

/// A stable, order-independent name for an opaque index: `idx_<table>_raw_<hash>` over
/// the canonical `raw(…)` body, so reordering a model's indexes never looks like a
/// rename and two different bodies never collide in the snapshot.
pub(crate) fn raw_index_name(table: &str, spec: &RawSpec) -> String {
    let mut h = 0xcbf2_9ce4_8422_2325u64;
    for b in spec.canonical().as_bytes() {
        h ^= *b as u64;
        h = h.wrapping_mul(0x0000_0100_0000_01b3);
    }
    format!("idx_{table}_raw_{:08x}", h as u32)
}

/// Is this index one MariaDB expresses as a table clause? The opaque `raw` form is
/// always a standalone `CREATE INDEX` (its body is not a column list).
fn inline_on_mariadb(spec: &IndexSpec) -> bool {
    spec.raw.is_none()
}

/// One `CREATE TABLE` for a model: columns in declaration order, then the table
/// constraints (primary key, uniques, and — on MariaDB — the indexes inline). On
/// SQLite the indexes follow as separate `CREATE INDEX` statements (no inline form).
fn create_table(schema: &CheckedSchema, model: &RModel, dialect: Dialect) -> String {
    let mut lines: Vec<String> = Vec::new();

    // Columns: scalars and forward-relation FKs. Inverse edges store nothing here.
    for mem in &model.members {
        match &mem.kind {
            MemberKind::Scalar {
                ty,
                optional,
                many,
                column,
                default,
                enum_name,
                raw_type,
                unique: _,
            } => {
                let en = enum_name.as_deref().and_then(|n| schema.enum_(n));
                // An opaque column's DB type is the literal the author wrote.
                let col_ty = match raw_type {
                    Some(spec) => spec
                        .for_dialect(dialect.name())
                        .unwrap_or_default()
                        .to_string(),
                    None => sql_type(*ty, *many, dialect),
                };
                lines.push(column_line(
                    column,
                    &col_ty,
                    *optional,
                    default.as_ref(),
                    en,
                    dialect,
                ));
            }
            MemberKind::Forward {
                fk_col,
                optional,
                target,
                ..
            } => {
                // FK column type mirrors the target's primary-key type (default uuid).
                let ty = fk_type(schema, target, dialect);
                lines.push(column_line(fk_col, &ty, *optional, None, None, dialect));
            }
            MemberKind::Inverse { .. } => {}
        }
    }

    // Primary key — `id`, the first member `skeleton` inserts. A `@no_id` (keyless
    // legacy) table has none.
    if !model.no_id {
        lines.push(format!("PRIMARY KEY ({})", dialect.quote("id")));
    }

    // Column-level `(unique)` constraints, in member order. All dialects accept the
    // inline `CONSTRAINT … UNIQUE (…)` table clause.
    for mem in &model.members {
        if let MemberKind::Scalar {
            column,
            unique: true,
            ..
        } = &mem.kind
        {
            lines.push(format!(
                "CONSTRAINT {name} UNIQUE ({col})",
                name = dialect.quote(&constraint_name(
                    "uq",
                    &model.table,
                    std::slice::from_ref(column)
                )),
                col = dialect.quote(column),
            ));
        }
    }

    // Enum columns carry a DB-native CHECK constraint (`col IN ('v1', …)`) — the
    // dialect-honest form on every target (SQLite has no native enum type; Postgres's
    // `CREATE TYPE` and MariaDB's inline `ENUM(…)` both make a variant change an awkward
    // `ALTER TYPE`/`MODIFY`, so a text column + CHECK is the uniform, migration-simple
    // representation). Membership is also enforced by the DSL layer (E0154).
    for mem in &model.members {
        if let MemberKind::Scalar {
            column,
            enum_name: Some(en_name),
            ..
        } = &mem.kind
        {
            if let Some(en) = schema.enum_(en_name) {
                let values: Vec<String> = en
                    .variants
                    .iter()
                    .map(|v| enum_value_sql(&v.value))
                    .collect();
                lines.push(enum_check_clause(dialect, &model.table, column, &values));
            }
        }
    }

    let specs = index_specs(model, dialect);

    // MariaDB inlines the indexes as `KEY` / `UNIQUE KEY` clauses; SQLite and Postgres
    // have no inline form, so they trail the table as `CREATE INDEX` statements.
    if dialect == Dialect::MariaDb {
        for spec in specs.iter().filter(|s| inline_on_mariadb(s)) {
            lines.push(inline_index_clause(dialect, spec));
        }
    }

    let body = lines
        .iter()
        .map(|l| format!("  {l}"))
        .collect::<Vec<_>>()
        .join(",\n");
    let mut out = format!(
        "CREATE TABLE {} (\n{body}\n);\n",
        dialect.quote(&model.table)
    );

    for spec in specs
        .iter()
        .filter(|s| dialect != Dialect::MariaDb || !inline_on_mariadb(s))
    {
        out.push_str(&create_index_stmt(dialect, &model.table, spec));
    }
    out
}

/// A single column definition: `` `name` TYPE (NOT NULL|NULL) [DEFAULT x] ``. `en` is the
/// column's enum, when enum-typed, so a variant default renders as its wire value.
fn column_line(
    column: &str,
    ty: &str,
    optional: bool,
    default: Option<&DefaultVal>,
    en: Option<&based_sema::REnum>,
    dialect: Dialect,
) -> String {
    let mut s = format!(
        "{} {ty} {}",
        dialect.quote(column),
        if optional { "NULL" } else { "NOT NULL" }
    );
    if let Some(dv) = default {
        s.push_str(" DEFAULT ");
        s.push_str(&render_default(dv, en, dialect));
    }
    s
}

/// A declared/inferred index as an inline MariaDB table clause. A unique index
/// becomes `UNIQUE KEY`, otherwise `KEY`. The name keeps field-derived readability;
/// the column list is physical (a relation field is its FK column, resolved upstream).
fn inline_index_clause(dialect: Dialect, spec: &IndexSpec) -> String {
    let cols = spec
        .columns
        .iter()
        .map(|c| dialect.quote(c))
        .collect::<Vec<_>>()
        .join(", ");
    // MariaDB spells its two exotic access methods as index *kinds* (`FULLTEXT KEY`),
    // and its B-tree/hash choice as a trailing `USING` — never Postgres's leading form.
    let (kind, using) = match spec.method.as_deref() {
        Some("fulltext") => ("FULLTEXT KEY", String::new()),
        Some("spatial") => ("SPATIAL KEY", String::new()),
        Some(m) => (
            if spec.unique { "UNIQUE KEY" } else { "KEY" },
            format!(" USING {}", m.to_uppercase()),
        ),
        None => (
            if spec.unique { "UNIQUE KEY" } else { "KEY" },
            String::new(),
        ),
    };
    format!(
        "{kind} {name} ({cols}){using}",
        name = dialect.quote(&spec.name)
    )
}

/// A declared/inferred index as a standalone `CREATE INDEX` statement (SQLite and
/// Postgres have no inline table-index clause). A unique index becomes `CREATE UNIQUE
/// INDEX`.
fn create_index_stmt(dialect: Dialect, table: &str, spec: &IndexSpec) -> String {
    format!("{};\n", create_index_sql(dialect, table, spec))
}

/// A standalone `CREATE [UNIQUE] INDEX`, bare (no trailing `;`). An opaque index's body
/// rides verbatim in place of the column list; `using <method>` renders as Postgres's
/// leading `USING <m>` (MariaDB spells methods inline — see `inline_index_clause`).
fn create_index_sql(dialect: Dialect, table: &str, spec: &IndexSpec) -> String {
    let kind = if spec.unique {
        "CREATE UNIQUE INDEX"
    } else {
        "CREATE INDEX"
    };
    let name = dialect.quote(&spec.name);
    let table_q = dialect.quote(table);
    if let Some(raw) = &spec.raw {
        return format!("{kind} {name} ON {table_q} {raw}");
    }
    let cols = spec
        .columns
        .iter()
        .map(|c| dialect.quote(c))
        .collect::<Vec<_>>()
        .join(", ");
    match spec.method.as_deref() {
        Some(m) => format!("{kind} {name} ON {table_q} USING {m} ({cols})"),
        None => format!("{kind} {name} ON {table_q} ({cols})"),
    }
}

/// Physical column backing a field: a scalar's column, or a forward relation's FK.
/// Falls back to the field name (an inverse edge or unknown field — the latter
/// sema already rejected via `E0122`).
fn physical_col(model: &RModel, field: &str) -> String {
    match model.member(field).map(|m| &m.kind) {
        Some(MemberKind::Scalar { column, .. }) => column.clone(),
        Some(MemberKind::Forward { fk_col, .. }) => fk_col.clone(),
        _ => field.to_string(),
    }
}

/// A single enum wire value rendered as a SQL literal: a string quoted, an int bare.
pub(crate) fn enum_value_sql(v: &based_sema::EnumValue) -> String {
    match v {
        based_sema::EnumValue::Str(s) => format!("'{}'", s.replace('\'', "''")),
        based_sema::EnumValue::Int(n) => n.to_string(),
    }
}

/// The `CONSTRAINT ck_<table>_<col> CHECK (<col> IN (v1, …))` clause enforcing an enum
/// column's values (each already rendered as a SQL literal — `'paid'` or `0`). Shared
/// with the migration renderer so a from-scratch migration matches `based gen sql`.
pub(crate) fn enum_check_clause(
    dialect: Dialect,
    table: &str,
    column: &str,
    values: &[String],
) -> String {
    let list = values.join(", ");
    format!(
        "CONSTRAINT {name} CHECK ({col} IN ({list}))",
        name = dialect.quote(&constraint_name(
            "ck",
            table,
            std::slice::from_ref(&column.to_string())
        )),
        col = dialect.quote(column),
    )
}

/// Stable, readable constraint/index name: `<prefix>_<table>_<col1>_<col2>`.
fn constraint_name(prefix: &str, table: &str, columns: &[String]) -> String {
    let mut name = format!("{prefix}_{table}");
    for c in columns {
        name.push('_');
        name.push_str(c);
    }
    name
}

/// SQL column type for a scalar primitive, per dialect (see the module type table).
/// A to-many scalar has no columnar form, so it is stored as a JSON array — the
/// dialect's JSON type (`JSON` on MariaDB, `TEXT` on SQLite, which has no JSON type).
/// `pub(crate)` so the migration renderer (`migrate`) maps neutral snapshot types
/// through the *same* table — one dialect type map, never a second that can drift.
pub(crate) fn sql_type(ty: Primitive, many: bool, dialect: Dialect) -> String {
    match dialect {
        Dialect::MariaDb => {
            if many {
                return "JSON".into();
            }
            match ty {
                Primitive::Text => "VARCHAR(255)".into(),
                Primitive::Int => "BIGINT".into(),
                Primitive::Bool => "BOOLEAN".into(),
                Primitive::Timestamp => "DATETIME".into(),
                Primitive::Date => "DATE".into(),
                Primitive::Json => "JSON".into(),
                Primitive::Uuid | Primitive::Id => "UUID".into(),
                Primitive::Float => "DOUBLE".into(),
                Primitive::Decimal { precision, scale } => format!("DECIMAL({precision}, {scale})"),
            }
        }
        // SQLite has a tiny type set (its dynamic typing means these are affinities,
        // not constraints). The mapping mirrors the runtime `SqliteDb` value mapping:
        // bool ⇒ integer 0/1, json/uuid/timestamp/date ⇒ text. `decimal` takes TEXT
        // affinity (not SQLite's lossy NUMERIC): it is stored + returned as its exact
        // string, so no digit is lost and the wire form stays a JSON string as it is on
        // MariaDB/Postgres. (Comparison is therefore lexicographic on SQLite — a fixed
        // affinity limitation; production dialects use a true numeric DECIMAL.)
        Dialect::Sqlite => {
            if many {
                return "TEXT".into();
            }
            match ty {
                Primitive::Text => "TEXT".into(),
                Primitive::Int | Primitive::Bool => "INTEGER".into(),
                Primitive::Timestamp | Primitive::Date => "TEXT".into(),
                Primitive::Json => "TEXT".into(),
                Primitive::Uuid | Primitive::Id => "TEXT".into(),
                Primitive::Float => "REAL".into(),
                Primitive::Decimal { .. } => "TEXT".into(),
            }
        }
        // Postgres has a rich, standards-track type set: real `BOOLEAN`, native `UUID`,
        // tz-aware `TIMESTAMPTZ`, and `JSONB` (the indexable/`@>`-queryable JSON form —
        // matching the DML `has` -> `@>` lowering). `text` is unbounded (no VARCHAR cap).
        Dialect::Postgres => {
            if many {
                return "JSONB".into();
            }
            match ty {
                Primitive::Text => "TEXT".into(),
                Primitive::Int => "BIGINT".into(),
                Primitive::Bool => "BOOLEAN".into(),
                Primitive::Timestamp => "TIMESTAMPTZ".into(),
                Primitive::Date => "DATE".into(),
                Primitive::Json => "JSONB".into(),
                Primitive::Uuid | Primitive::Id => "UUID".into(),
                Primitive::Float => "DOUBLE PRECISION".into(),
                Primitive::Decimal { precision, scale } => format!("NUMERIC({precision}, {scale})"),
            }
        }
    }
}

/// The SQL type of a relation's FK column — the target model's primary-key type.
/// Defaults to the dialect's id type (from the implicit `id`) when the target or
/// its key is missing, which sema would already have flagged.
fn fk_type(schema: &CheckedSchema, target: &str, dialect: Dialect) -> String {
    match schema
        .model(target)
        .and_then(|m| m.member("id"))
        .map(|m| &m.kind)
    {
        Some(MemberKind::Scalar { ty, .. }) => sql_type(*ty, false, dialect),
        _ => sql_type(Primitive::Uuid, false, dialect),
    }
}

/// Render a `(default …)` value as a SQL literal / expression, per dialect. Only the
/// bool literal differs: MariaDB has `TRUE`/`FALSE` keywords; SQLite stores bools as
/// integers, so a bool default renders `1`/`0`.
fn render_default(dv: &DefaultVal, en: Option<&based_sema::REnum>, dialect: Dialect) -> String {
    match dv {
        DefaultVal::Lit(Literal::Str(s)) => format!("'{}'", s.replace('\'', "''")),
        DefaultVal::Lit(Literal::Int(i)) => i.to_string(),
        DefaultVal::Lit(Literal::Decimal(s)) => s.clone(),
        DefaultVal::Lit(Literal::Bool(b)) => dialect.bool_lit(*b).to_string(),
        DefaultVal::Lit(Literal::Null) => "NULL".to_string(),
        // `now()` is the only value-position function (ir::KNOWN_FUNCS).
        DefaultVal::Func(_) => "CURRENT_TIMESTAMP".to_string(),
        // An enum default (`default pending`) is its variant's wire value: a quoted
        // string for a string enum, a bare integer for an int enum.
        DefaultVal::Variant(v) => en
            .and_then(|e| e.wire_of(&v.node))
            .map(enum_value_sql)
            .unwrap_or_else(|| format!("'{}'", v.node.replace('\'', "''"))),
    }
}
