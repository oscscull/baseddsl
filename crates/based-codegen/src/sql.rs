/// Render the whole schema as a DDL script (statements separated by blank lines).
pub fn ddl(schema: &CheckedSchema, dialect: Dialect) -> String {
    let mut out = String::new();
    out.push_str(&format!(
        "-- Generated by `based gen sql` (dialect: {}). Do not edit by hand.\n",
        dialect.name()
    ));
    for model in &schema.models {
        out.push('\n');
        out.push_str(&create_table(schema, model, dialect));
    }
    out
}

/// The set of index-like clauses a model carries, resolved to physical columns and
/// names. Shared by both dialects: MariaDB inlines them into the `CREATE TABLE`;
/// SQLite emits them as trailing `CREATE INDEX` statements.
struct IndexSpec {
    name: String,
    columns: Vec<String>,
    unique: bool,
}

/// Every declared + inferred index on a model, in emission order (declared first,
/// then the sema-inferred join-key baseline). Column names are already physical.
fn index_specs(model: &RModel) -> Vec<IndexSpec> {
    let mut specs = Vec::new();

    // Declared `@index`es (composite or single; unique or plain).
    for idx in &model.indexes {
        specs.push(IndexSpec {
            name: constraint_name(
                if idx.unique { "uq" } else { "idx" },
                &model.table,
                &idx.columns,
            ),
            columns: idx.columns.iter().map(|c| physical_col(model, c)).collect(),
            unique: idx.unique,
        });
    }

    // Inferred baseline indexes (indexing.md, D15): join keys of traversed inverse
    // edges, deduped by sema against declared structure. Neither MariaDB nor SQLite
    // has partial indexes, so "predicate-leading" means the soft-delete column is
    // physically prepended — the engine filters it on every generated query.
    for idx in &model.inferred_indexes {
        let mut columns = idx.columns.clone();
        if let Some(sd) = &model.soft_delete {
            columns.insert(0, sd.field.clone());
        }
        specs.push(IndexSpec {
            name: constraint_name("inf", &model.table, &columns),
            columns: columns.iter().map(|c| physical_col(model, c)).collect(),
            unique: false,
        });
    }

    specs
}

/// One `CREATE TABLE` for a model: columns in declaration order, then the table
/// constraints (primary key, uniques, and — on MariaDB — the indexes inline). On
/// SQLite the indexes follow as separate `CREATE INDEX` statements (no inline form).
fn create_table(schema: &CheckedSchema, model: &RModel, dialect: Dialect) -> String {
    let mut lines: Vec<String> = Vec::new();

    // Columns: scalars and forward-relation FKs. Inverse edges store nothing here.
    for mem in &model.members {
        match &mem.kind {
            MemberKind::Scalar {
                ty,
                optional,
                many,
                column,
                default,
                ..
            } => {
                lines.push(column_line(
                    column,
                    sql_type(*ty, *many, dialect),
                    *optional,
                    default.as_ref(),
                    dialect,
                ));
            }
            MemberKind::Forward {
                fk_col,
                optional,
                target,
                ..
            } => {
                // FK column type mirrors the target's primary-key type (default uuid).
                let ty = fk_type(schema, target, dialect);
                lines.push(column_line(fk_col, ty, *optional, None, dialect));
            }
            MemberKind::Inverse { .. } => {}
        }
    }

    // Primary key — always `id` (D2). It is the first member `skeleton` inserts.
    lines.push("PRIMARY KEY (`id`)".to_string());

    // Column-level `(unique)` constraints, in member order. Both dialects accept the
    // inline `CONSTRAINT … UNIQUE (…)` table clause.
    for mem in &model.members {
        if let MemberKind::Scalar {
            column,
            unique: true,
            ..
        } = &mem.kind
        {
            lines.push(format!(
                "CONSTRAINT `{name}` UNIQUE (`{column}`)",
                name = constraint_name("uq", &model.table, std::slice::from_ref(column)),
            ));
        }
    }

    let specs = index_specs(model);

    // MariaDB inlines the indexes as `KEY` / `UNIQUE KEY` clauses; SQLite has no
    // inline form, so they trail the table as `CREATE INDEX` statements.
    if dialect == Dialect::MariaDb {
        for spec in &specs {
            lines.push(inline_index_clause(spec));
        }
    }

    let body = lines
        .iter()
        .map(|l| format!("  {l}"))
        .collect::<Vec<_>>()
        .join(",\n");
    let mut out = format!("CREATE TABLE `{}` (\n{body}\n);\n", model.table);

    if dialect == Dialect::Sqlite {
        for spec in &specs {
            out.push_str(&create_index_stmt(&model.table, spec));
        }
    }
    out
}

/// A single column definition: `` `name` TYPE (NOT NULL|NULL) [DEFAULT x] ``.
fn column_line(
    column: &str,
    ty: &str,
    optional: bool,
    default: Option<&DefaultVal>,
    dialect: Dialect,
) -> String {
    let mut s = format!(
        "`{column}` {ty} {}",
        if optional { "NULL" } else { "NOT NULL" }
    );
    if let Some(dv) = default {
        s.push_str(" DEFAULT ");
        s.push_str(&render_default(dv, dialect));
    }
    s
}

/// A declared/inferred index as an inline MariaDB table clause. A unique index
/// becomes `UNIQUE KEY`, otherwise `KEY`. The name keeps field-derived readability;
/// the column list is physical (a relation field is its FK column, resolved upstream).
fn inline_index_clause(spec: &IndexSpec) -> String {
    let cols = spec
        .columns
        .iter()
        .map(|c| format!("`{c}`"))
        .collect::<Vec<_>>()
        .join(", ");
    let kind = if spec.unique { "UNIQUE KEY" } else { "KEY" };
    format!("{kind} `{name}` ({cols})", name = spec.name)
}

/// A declared/inferred index as a standalone SQLite `CREATE INDEX` statement (SQLite
/// has no inline table-index clause). A unique index becomes `CREATE UNIQUE INDEX`.
fn create_index_stmt(table: &str, spec: &IndexSpec) -> String {
    let cols = spec
        .columns
        .iter()
        .map(|c| format!("`{c}`"))
        .collect::<Vec<_>>()
        .join(", ");
    let kind = if spec.unique {
        "CREATE UNIQUE INDEX"
    } else {
        "CREATE INDEX"
    };
    format!("{kind} `{name}` ON `{table}` ({cols});\n", name = spec.name)
}

/// Physical column backing a field: a scalar's column, or a forward relation's FK.
/// Falls back to the field name (an inverse edge or unknown field — the latter
/// sema already rejected via `E0122`).
fn physical_col(model: &RModel, field: &str) -> String {
    match model.member(field).map(|m| &m.kind) {
        Some(MemberKind::Scalar { column, .. }) => column.clone(),
        Some(MemberKind::Forward { fk_col, .. }) => fk_col.clone(),
        _ => field.to_string(),
    }
}

/// Stable, readable constraint/index name: `<prefix>_<table>_<col1>_<col2>`.
fn constraint_name(prefix: &str, table: &str, columns: &[String]) -> String {
    let mut name = format!("{prefix}_{table}");
    for c in columns {
        name.push('_');
        name.push_str(c);
    }
    name
}

/// SQL column type for a scalar primitive, per dialect (see the module type table).
/// A to-many scalar has no columnar form, so it is stored as a JSON array — the
/// dialect's JSON type (`JSON` on MariaDB, `TEXT` on SQLite, which has no JSON type).
fn sql_type(ty: Primitive, many: bool, dialect: Dialect) -> &'static str {
    match dialect {
        Dialect::MariaDb => {
            if many {
                return "JSON";
            }
            match ty {
                Primitive::Text => "VARCHAR(255)",
                Primitive::Int => "BIGINT",
                Primitive::Bool => "BOOLEAN",
                Primitive::Timestamp => "DATETIME",
                Primitive::Date => "DATE",
                Primitive::Json => "JSON",
                Primitive::Uuid | Primitive::Id => "UUID",
            }
        }
        // SQLite has a tiny type set (its dynamic typing means these are affinities,
        // not constraints). The mapping mirrors the runtime `SqliteDb` value mapping
        // (D27): bool ⇒ integer 0/1, json/uuid/timestamp/date ⇒ text.
        Dialect::Sqlite => {
            if many {
                return "TEXT";
            }
            match ty {
                Primitive::Text => "TEXT",
                Primitive::Int | Primitive::Bool => "INTEGER",
                Primitive::Timestamp | Primitive::Date => "TEXT",
                Primitive::Json => "TEXT",
                Primitive::Uuid | Primitive::Id => "TEXT",
            }
        }
    }
}

/// The SQL type of a relation's FK column — the target model's primary-key type.
/// Defaults to the dialect's id type (from the implicit `id`, D1) when the target or
/// its key is missing, which sema would already have flagged.
fn fk_type(schema: &CheckedSchema, target: &str, dialect: Dialect) -> &'static str {
    match schema
        .model(target)
        .and_then(|m| m.member("id"))
        .map(|m| &m.kind)
    {
        Some(MemberKind::Scalar { ty, .. }) => sql_type(*ty, false, dialect),
        _ => sql_type(Primitive::Uuid, false, dialect),
    }
}

/// Render a `(default …)` value as a SQL literal / expression, per dialect. Only the
/// bool literal differs: MariaDB has `TRUE`/`FALSE` keywords; SQLite stores bools as
/// integers (D27), so a bool default renders `1`/`0`.
fn render_default(dv: &DefaultVal, dialect: Dialect) -> String {
    match dv {
        DefaultVal::Lit(Literal::Str(s)) => format!("'{}'", s.replace('\'', "''")),
        DefaultVal::Lit(Literal::Int(i)) => i.to_string(),
        DefaultVal::Lit(Literal::Float(f)) => f.to_string(),
        DefaultVal::Lit(Literal::Bool(b)) => match dialect {
            Dialect::MariaDb => if *b { "TRUE" } else { "FALSE" }.to_string(),
            Dialect::Sqlite => if *b { "1" } else { "0" }.to_string(),
        },
        DefaultVal::Lit(Literal::Null) => "NULL".to_string(),
        // `now()` is the only value-position function (ir::KNOWN_FUNCS).
        DefaultVal::Func(_) => "CURRENT_TIMESTAMP".to_string(),
    }
}
