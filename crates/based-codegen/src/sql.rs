//! SQL DDL generation (M2): `CheckedSchema` -> `CREATE TABLE` statements.
//!
//! One `CREATE TABLE` per model, emitted in schema order (deterministic). Each
//! table carries its columns, the `id` primary key, `(unique)` constraints, and
//! declared `@index`es. No foreign-key constraints are emitted — relations.md makes
//! them opt-in, so the FK *column* exists but no `FOREIGN KEY` clause references it.
//!
//! Besides declared structure, each table carries the sema-inferred baseline
//! indexes (indexing.md, D15): join-key indexes for inverse edges the access
//! layer traverses, named `inf_…` so engine-owned keys are distinguishable from
//! declared `idx_…` ones. On a `@soft_delete` model the soft-delete column is
//! prepended (predicate-leading — MariaDB has no partial indexes). Filter-path
//! indexes are *not* auto-created; they surface as the W0103 lint instead.
//!
//! ## Type mapping (MariaDB)
//! | primitive   | SQL            | note |
//! |-------------|----------------|------|
//! | `text`      | `VARCHAR(255)` | bounded so it is directly index/unique-able |
//! | `int`       | `BIGINT`       | avoids silent overflow on money/counts |
//! | `bool`      | `BOOLEAN`      | MariaDB alias for `TINYINT(1)` |
//! | `timestamp` | `DATETIME`     | dodges `TIMESTAMP`'s implicit ON UPDATE + 2038 |
//! | `date`      | `DATE`         | |
//! | `json`      | `JSON`         | |
//! | `uuid`/`Id` | `UUID`         | native MariaDB type (D1); app-generated, no default |
//!
//! A to-many scalar (`text[]`) has no columnar form; it maps to `JSON` (a JSON array).

use based_ast::{DefaultVal, Literal, Primitive};
use based_sema::{CheckedSchema, MemberKind, RIndex, RModel};

use crate::Dialect;

pub mod dml;
pub mod mutations;

/// Render the whole schema as a DDL script (statements separated by blank lines).
pub fn ddl(schema: &CheckedSchema, dialect: Dialect) -> String {
    let Dialect::MariaDb = dialect;
    let mut out = String::new();
    out.push_str("-- Generated by `based gen sql` (dialect: mariadb). Do not edit by hand.\n");
    for model in &schema.models {
        out.push('\n');
        out.push_str(&create_table(schema, model));
    }
    out
}

/// One `CREATE TABLE` for a model: columns in declaration order, then the table
/// constraints (primary key, uniques, indexes).
fn create_table(schema: &CheckedSchema, model: &RModel) -> String {
    let mut lines: Vec<String> = Vec::new();

    // Columns: scalars and forward-relation FKs. Inverse edges store nothing here.
    for mem in &model.members {
        match &mem.kind {
            MemberKind::Scalar {
                ty,
                optional,
                many,
                column,
                default,
                ..
            } => {
                lines.push(column_line(
                    column,
                    sql_type(*ty, *many),
                    *optional,
                    default.as_ref(),
                ));
            }
            MemberKind::Forward {
                fk_col,
                optional,
                target,
                ..
            } => {
                // FK column type mirrors the target's primary-key type (default uuid).
                let ty = fk_type(schema, target);
                lines.push(column_line(fk_col, ty, *optional, None));
            }
            MemberKind::Inverse { .. } => {}
        }
    }

    // Primary key — always `id` (D2). It is the first member `skeleton` inserts.
    lines.push("PRIMARY KEY (`id`)".to_string());

    // Column-level `(unique)` constraints, in member order.
    for mem in &model.members {
        if let MemberKind::Scalar {
            column,
            unique: true,
            ..
        } = &mem.kind
        {
            lines.push(format!(
                "CONSTRAINT `{name}` UNIQUE (`{column}`)",
                name = constraint_name("uq", &model.table, std::slice::from_ref(column)),
            ));
        }
    }

    // Declared `@index`es (composite or single; unique or plain).
    for idx in &model.indexes {
        lines.push(index_clause(model, idx));
    }

    // Inferred baseline indexes (indexing.md, D15): join keys of traversed
    // inverse edges, deduped by sema against declared structure. MariaDB has no
    // partial indexes, so "predicate-leading" means the soft-delete column is
    // physically prepended — the engine filters it on every generated query.
    for idx in &model.inferred_indexes {
        let mut columns = idx.columns.clone();
        if let Some(sd) = &model.soft_delete {
            columns.insert(0, sd.field.clone());
        }
        let cols = columns
            .iter()
            .map(|c| format!("`{}`", physical_col(model, c)))
            .collect::<Vec<_>>()
            .join(", ");
        let name = constraint_name("inf", &model.table, &columns);
        lines.push(format!("KEY `{name}` ({cols})"));
    }

    let body = lines
        .iter()
        .map(|l| format!("  {l}"))
        .collect::<Vec<_>>()
        .join(",\n");
    format!("CREATE TABLE `{}` (\n{body}\n);\n", model.table)
}

/// A single column definition: `` `name` TYPE (NOT NULL|NULL) [DEFAULT x] ``.
fn column_line(column: &str, ty: &str, optional: bool, default: Option<&DefaultVal>) -> String {
    let mut s = format!(
        "`{column}` {ty} {}",
        if optional { "NULL" } else { "NOT NULL" }
    );
    if let Some(dv) = default {
        s.push_str(" DEFAULT ");
        s.push_str(&render_default(dv));
    }
    s
}

/// A declared index as an inline table clause. Unique indexes become `UNIQUE KEY`.
/// Index columns name *fields*; a relation field indexes its FK column (`<f>_id`).
/// The index *name* keeps the field names (readable); the column list is physical.
fn index_clause(model: &RModel, idx: &RIndex) -> String {
    let cols = idx
        .columns
        .iter()
        .map(|c| format!("`{}`", physical_col(model, c)))
        .collect::<Vec<_>>()
        .join(", ");
    let prefix = if idx.unique { "uq" } else { "idx" };
    let name = constraint_name(prefix, &model.table, &idx.columns);
    let kind = if idx.unique { "UNIQUE KEY" } else { "KEY" };
    format!("{kind} `{name}` ({cols})")
}

/// Physical column backing a field: a scalar's column, or a forward relation's FK.
/// Falls back to the field name (an inverse edge or unknown field — the latter
/// sema already rejected via `E0122`).
fn physical_col(model: &RModel, field: &str) -> String {
    match model.member(field).map(|m| &m.kind) {
        Some(MemberKind::Scalar { column, .. }) => column.clone(),
        Some(MemberKind::Forward { fk_col, .. }) => fk_col.clone(),
        _ => field.to_string(),
    }
}

/// Stable, readable constraint/index name: `<prefix>_<table>_<col1>_<col2>`.
fn constraint_name(prefix: &str, table: &str, columns: &[String]) -> String {
    let mut name = format!("{prefix}_{table}");
    for c in columns {
        name.push('_');
        name.push_str(c);
    }
    name
}

/// SQL column type for a scalar primitive. A to-many scalar has no columnar form,
/// so it is stored as a JSON array.
fn sql_type(ty: Primitive, many: bool) -> &'static str {
    if many {
        return "JSON";
    }
    match ty {
        Primitive::Text => "VARCHAR(255)",
        Primitive::Int => "BIGINT",
        Primitive::Bool => "BOOLEAN",
        Primitive::Timestamp => "DATETIME",
        Primitive::Date => "DATE",
        Primitive::Json => "JSON",
        Primitive::Uuid | Primitive::Id => "UUID",
    }
}

/// The SQL type of a relation's FK column — the target model's primary-key type.
/// Defaults to `UUID` (the implicit `id` type, D1) when the target or its key is
/// missing, which sema would already have flagged.
fn fk_type(schema: &CheckedSchema, target: &str) -> &'static str {
    match schema
        .model(target)
        .and_then(|m| m.member("id"))
        .map(|m| &m.kind)
    {
        Some(MemberKind::Scalar { ty, .. }) => sql_type(*ty, false),
        _ => "UUID",
    }
}

/// Render a `(default …)` value as a SQL literal / expression.
fn render_default(dv: &DefaultVal) -> String {
    match dv {
        DefaultVal::Lit(Literal::Str(s)) => format!("'{}'", s.replace('\'', "''")),
        DefaultVal::Lit(Literal::Int(i)) => i.to_string(),
        DefaultVal::Lit(Literal::Float(f)) => f.to_string(),
        DefaultVal::Lit(Literal::Bool(b)) => if *b { "TRUE" } else { "FALSE" }.to_string(),
        DefaultVal::Lit(Literal::Null) => "NULL".to_string(),
        // `now()` is the only value-position function (ir::KNOWN_FUNCS).
        DefaultVal::Func(_) => "CURRENT_TIMESTAMP".to_string(),
    }
}
